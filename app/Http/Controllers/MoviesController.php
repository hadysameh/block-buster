     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        date_default_timezone_set('Africa/Cairo');        

        $movies = Movie::join('shows',function($join){
            $join->on('movies.id','=','shows.movie_id')
            ->where('shows.day', '=',date('Y-m-d',time()));
        })        
            ->select('movies.id','movies.movie_name','movies.image','movies.rate')
            ->groupBy('movies.id','movies.movie_name','movies.image','movies.rate')
            ->get();

        return view('home',['movies' => $movies]);
    }

